'''Кортеж уникальных
Напишите программу, которая обрабатывает кортеж чисел. Программа должна вернуть кортеж, в котором будут только
уникальные элементы. Не используйте неизученные коллекции.
Данные:
numbers = (1, 2, 3, 2, 1, 4, 5, 3, 6)
Пример вывода:
Уникальные элементы: (1, 2, 3, 4, 5, 6)'''
numbers = (1, 2, 3, 2, 1, 4, 5, 3, 6)
uniq = ()
for n in numbers:
    if n not in uniq:
        uniq += (n,)
print("Уникальные элементы: ", uniq)

'''Напишите программу, которая обрабатывает кортеж чисел. Программа должна вернуть кортеж, состоящий из элементов, 
которые больше среднего значения всех элементов исходного кортежа.
Данные: 
numbers = (10, 15, 20, 25, 30)
Пример вывода: 
Элементы больше среднего: (25, 30)'''
numbers = (10, 15, 20, 25, 30)
more_avg = ()
avg = sum(numbers) / len(numbers)
for num in numbers:
    if num > avg:
        more_avg += (num,)
print(*more_avg)

